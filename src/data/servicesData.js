// Get all unique brands across all categories
export const getAllBrands = () => {
    const allBrands = servicesData.flatMap(cat => cat.brands);
    return [...new Set(allBrands)].sort();
};

// Get brands for a specific category
export const getBrandsByCategory = (category) => {
    if (category === "All") {
        return getAllBrands();
    }
    const categoryData = servicesData.find(cat => cat.category === category);
    return categoryData ? categoryData.brands : [];
};

// Get all category names
export const getCategories = () => {
    return ["All", ...servicesData.map(cat => cat.category)];
};

// Get all brands with their logos for display
export const getAllBrandsForDisplay = () => {
    return getAllBrands();
};

// Get logo path for a brand
export const getBrandLogo = (brandName) => {
    const path = brandLogos[brandName];
    if (!path) return null;
    const cleanPath = path.startsWith('/') ? path.slice(1) : path;
    return import.meta.env.BASE_URL + cleanPath;
};

// Get website URL for a brand
export const getBrandWebsite = (brandName) => {
    return brandWebsites[brandName] || null;
};
